CREATE view all_activities as
				select a.activity_play_name as Play_Name, t.theatre_address as Theatre_Location, t.theatre_name as Theatre_Name, a.activity_date as Activity_Date
				from theatre t, activity a
				where a.activity_theatre_address = t.theatre_address;

CREATE view all_customers as
				select c.customer_name as Customer_FullName, c.birthday as Customer_Birthday, a.activity_play_name as Play_Name, a.activity_date Play_Date, t.theatre_name as Theatre_Name
				from customer c, activity a, theatre t, ticket tct
				where c.id = tct.id and a.activity_theatre_address = t.theatre_address and tct.ticket_date = a.activity_date;
			
create view sold_tickets as
				select c.customer_name as CustomerFullName, tct.id as TicketID, a.activity_play_name as Play_Name, t.theatre_name as Theatre_Name, tct.ticket_price as TicketPrice
				from theatre t, activity a, ticket tct, customer c
				where a.activity_theatre_address = t.theatre_address and tct.id = c.id and tct.ticket_date = a.activity_date and tct.id between 0 and 100;
                  